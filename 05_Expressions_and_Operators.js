// A fragment of code that produces a value is called Expression

// Operators in JS

// ---------------AirthMetic Operators----------------

let a = 4;
let b = 8;

console.log("a + b", a + b);
console.log("a - b", a - b);
console.log("a / b", a / b);
console.log("a * b", a * b);
console.log("a % b", a % b);
console.log("a ** b", a ** b);

// Increament and Decreament

console.log("a++", a++);
console.log(a);

console.log("++b", ++b);

console.log("a--", a--);
console.log(a);

console.log("--b", --b);

// Assignment Operators

let assign = 8;
assign += 5;
console.log(assign);

// Comparison Operators

console.log(6 > 7);
console.log(6 < 7);
console.log(6 == 7);
console.log(6 !== 7);
console.log(7 === 7);

let x = 9;
let y = 2;

console.log(x > y && x == 9);
console.log(x > y || x != 9);
